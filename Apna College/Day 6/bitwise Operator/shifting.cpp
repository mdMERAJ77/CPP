#include<iostream>
using namespace std;
int main(){
	//right shift <<
	cout<<"right shift"<<endl;
	cout<<(9<<1)<<endl;
	cout<<(9<<2)<<endl;
	cout<<(9<<3)<<endl<<"\n";  //formula:- a*2^b or (a*2powerb)
	
	//left shift >>
	cout<<"left shift"<<endl;
	cout<<(9>>1)<<endl;
	cout<<(9>>2)<<endl;
	cout<<(9>>3);
	return 0;
}
